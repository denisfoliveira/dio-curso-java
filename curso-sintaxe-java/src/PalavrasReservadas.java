public class PalavrasReservadas {
   /*
    Palavras reservadas são identificadores de uma linguagem que já possuem uma finalidade específica, portanto não podem ser utilizadas para nomear variáveis, classes, métodos ou atributos. A linguagem java possui 52 palavras reservadas.

    abstract assert boolean break byte case catch char class const continue default do double else extends final finally float for goto if implements import instanceof int interface long native new package private protected public return short static strictfp super switch synchronized this throw throws transient try void volatil while

    - Controle de pacotes
        > import: importa pacotes ou classes para dentro do código
        > package: especifica a que pacote todas as classes de um arquivo pertencem

    - Modificadores de acesso
        > public: acesso de qualquer classe
        > private: acesso apenas dentro da classe
        > protected: acesso por classes do mesmo pacote e subclasses

    - Modificadores de classes, variáveis ou métodos
        > abstract: classe que não pode ser instanciada ou método que precisa ser implementado por uma subclasse não abstrata
        > class: especifica uma classe
        > extends: indica a superclasse que a subclasse está estentendo
        > final: impossibilita que uma classe seja estendida, que um método seja sobrescrito ou que uma variável seja reinicializada
        > implements: indica as interfaces que uma classe irá implementar
        > interface: especifica uma interface
        > native: indica que um método está escrito em uma linguagem dependente de plataforma, como o C
        > new: instancia um novo objeto, chamando seu construtor
        > static: faz um método ou variável pertencer à classe ao invés de às instâncias
        > strictfp: usado em frente a um método ou classe para indicar que os números de ponto flutuante seguirão as regras de ponto flutuante em todas as expressões
        > synchronized: indica que um método só pode ser acessado por uma thread de cada vez
        > transient: impede a serialização de campos
        > volatile: indica que uma variável pode ser alterada durante o uso de threads

    - Controle de fluxo dentro de um bloco de código
        > break: sai do bloco de código em que ele está
        > case: executa um bloco de código dependendo do teste do switch (switch case)
        > continue: pula a execução do código que viria após essa linha e vai para a próxima passagem do loop
        
 */ 

}
